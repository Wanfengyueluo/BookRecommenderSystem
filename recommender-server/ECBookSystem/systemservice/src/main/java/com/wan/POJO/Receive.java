package com.wan.POJO;

/**
 * @author wanfeng
 * @date 2020/2/1 17:09 接受参数的辅助类
 */
public class Receive {
  private int userId;
  private int bookId;
  private int score;

  public int getUserId() {
    return userId;
  }

  public void setUserId(int userId) {
    this.userId = userId;
  }

  public int getBookId() {
    return bookId;
  }

  public void setBookId(int bookId) {
    this.bookId = bookId;
  }

  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }
}
